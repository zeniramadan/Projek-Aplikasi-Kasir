    private void showdataitem() {
        DefaultTableModel model=(DefaultTableModel)tbl_menu.getModel();
        model.setRowCount(0);
        String carimenu=txt_cari.getText();
        try {
            String sql="SELECT * FROM tbl_dataitem WHERE INSTR(nama_item,'"+ carimenu +"')>0";
            ps=conn.prepareStatement(sql);
            rs=ps.executeQuery(sql);
            while(rs.next()) {
                int no=rs.getInt("No");
                String nama=rs.getString("nama_item");
                String kategori=rs.getString("kategori");
                int harga=rs.getInt("harga");

                Object[] RowData={nama,kategori,angkakoma.format(harga)};
                model.addRow(RowData);
            }
        } catch (Exception e) {
        }
    }
    
    private void HitungTotal(){
        int tot=0;
        for(int i=0;i<tbl_transaksi.getRowCount();i++){
            tot=tot+Integer.parseInt(tbl_transaksi.getValueAt(i, 5).toString().replaceAll(",", ""));
        }
        txt_totalbayar.setText(String.valueOf(angkakoma.format(tot)));
    }
    
    private void formatDesimal(){
        if(!txt_tunai.getText().isEmpty()){
            String ReplaceTunai=txt_tunai.getText().replaceAll("[^\\d]", "");
            double FormatDec=Double.parseDouble(ReplaceTunai);
            DecimalFormat decimalformat=new DecimalFormat("#,###,###");
            txt_tunai.setText(decimalformat.format(FormatDec));
            return;
        }
    }
    
    private void noFaktur(){
        String sql="SELECT MAX(no_faktur) FROM tbl_penjualan";
        try {
            st=conn.createStatement();
            rs=st.executeQuery(sql);
            rs.next();
            rs.getString("MAX(no_faktur)");
            if(rs.getString("MAX(no_faktur)")==null){
                lb_faktur.setText("INV00001");
            }else{
                long InvAngka=Long.parseLong(rs.getString("MAX(no_faktur)").substring(3,rs.getString("MAX(no_faktur)").length()));
                InvAngka++;
                lb_faktur.setText("INV"+String.format("%05d", InvAngka));
            }
        } catch (Exception e) {
        }
    }
    
    private void tanggal(){
        java.util.Date tgl = new java.util.Date();
        SimpleDateFormat FormatTanggal = new SimpleDateFormat("dd-MM-yyyy",Locale.getDefault());
        String TanggalSekarang = FormatTanggal.format(tgl);
        lb_tgl.setText(TanggalSekarang);
    }
    
