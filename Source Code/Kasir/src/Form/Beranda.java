    private void txt_tunaiKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_tunaiKeyReleased
        formatDesimal();
        if(txt_tunai.getText().isEmpty()){
            txt_kembali.setText("");
        }else{
            int cash=Integer.parseInt(txt_tunai.getText().replaceAll(",", ""));
            int totalbayar=Integer.parseInt(txt_totalbayar.getText().replaceAll(",", ""));
            int hasil=cash-totalbayar;
            
            if(cash >= totalbayar){
                String koma=angkakoma.format(hasil);
                txt_kembali.setText(String.valueOf(koma));
            }else{
                txt_kembali.setText("");
            }
        }
    }//GEN-LAST:event_txt_tunaiKeyReleased

    private void btn_bayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_bayarActionPerformed
        if(txt_kembali.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Anda belum memasukkan pembayawan","Validasi",JOptionPane.WARNING_MESSAGE);
        }else{
            try {
                DefaultTableModel model=(DefaultTableModel)tbl_transaksi.getModel();
                for(int i=0;i<tbl_transaksi.getRowCount();i++){
                    String nofaktur=model.getValueAt(i, 0).toString();
                    String tgl=model.getValueAt(i, 1).toString();
                    String item=model.getValueAt(i, 2).toString();
                    String qty=model.getValueAt(i, 3).toString();
                    String harga=model.getValueAt(i, 4).toString();
                    String jumlah=model.getValueAt(i, 5).toString();
                    
                    String sql="INSERT INTO tbl_penjualan (no_faktur,tgl,item,qty,harga,jumlah) VALUES(?,?,?,?,?,?)";
                    ps=conn.prepareStatement(sql);
                    ps.setString(1,nofaktur);
                    ps.setString(2,tgl);
                    ps.setString(3,item);
                    ps.setString(4,qty);
                    ps.setString(5,harga);
                    ps.setString(6,jumlah);
                    ps.executeUpdate();
                }
                JOptionPane.showMessageDialog(this, "Transaksi berhasil","Pembayaran",JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
            noFaktur();
        }
    }//GEN-LAST:event_btn_bayarActionPerformed

